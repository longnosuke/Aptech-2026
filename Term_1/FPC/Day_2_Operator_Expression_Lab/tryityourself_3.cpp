#include <stdio.h>

int main() {
  int s, m=3, n=5, r, t;
  float x=3.0, y;

  printf("t = %d\n", t = n/m);
  printf("r = %d\n", r = n%m);
  printf("y = %.2f\n", y = n/m);
  printf("t = %.2f\n", t = x*y-m/2);
  printf("x = %.2f\n", x = x*2.0);
  printf("s = %.2f\n", s = (m+n)/r);
  printf("y = %.2f\n", y = --n);
}



